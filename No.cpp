#include "No.h"

// ============================================================
// Construtor padrao
// ============================================================
No::No() {
    aresta = 0;
    proximo = 0;
}

// ============================================================
// Construtor parametrizado
// ============================================================
No::No(Aresta* aresta) {
    this->aresta = aresta;
    this->proximo = 0;
}

// ============================================================
// Destrutor
// ============================================================
No::~No() {
    aresta = 0;
    proximo = 0;
}

// ============================================================
// Metodos GET
// ============================================================
Aresta* No::getAresta() {
    return aresta;
}

No* No::getProximo() {
    return proximo;
}

// ============================================================
// Metodos SET
// ============================================================
void No::setAresta(Aresta* aresta) {
    this->aresta = aresta;
}

void No::setProximo(No* proximo) {
    this->proximo = proximo;
}
